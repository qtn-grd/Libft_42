#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s[len])
		len++;
	result = malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
